static const size_t rsa_private_der_len = sizeof(rsa_private_der);

void main(void) {
    struct jwt_builder builder;
    char jwt_buffer[1024];  
    int64_t iat = k_uptime_get() / 1000;
    int64_t exp = iat + 3600; 
    // Initialisation du JWT builder
    int res = jwt_init_builder(&builder, jwt_buffer, sizeof(jwt_buffer));
    if (res != 0) {
        printk("Erreur d'initialisation du JWT builder: %d\n", res);
        return;
    }

    // Ajout du payload au JWT
    res = jwt_add_payload(&builder, exp, iat, "Admin");
    if (res != 0) {
        printk("Erreur d'ajout de payload: %d\n", res);
        return;
    }

    // Signature du JWT avec la clé RSA
    res = jwt_sign(&builder, rsa_private_der, rsa_private_der_len);
    if (res != 0) {
        printk("Erreur de signature JWT: %d\n", res);
        return;
    }

    // Affichage du JWT généré
    printk("JWT généré: %s\n", jwt_buffer);
}